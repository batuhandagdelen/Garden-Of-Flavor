const uiElements = {
  menuList: document.querySelector("#menu-list"),
  categoryButtons: document.querySelectorAll(".category-button"),
  detailContainer: document.querySelector("#detail-container"),
};

// Menü elamanı render edecek fonksiyon

const renderMenuCard = (data) => {
  //Dışarıdan alınan datanın içerisindeki her eleman için bir html oluşturma

  uiElements.menuList.innerHTML = data
    .map(
      (a) => `      <a href="/detail.html?id=${
        a.id
      }" class="col-md-6 col-lg-4 text-decoration-none">
            <div class="card menu-card">
      
              <div class="position-relative">
                <img
                  src=${a.img}
                  class="card-img-top"
                  alt="menu-card-image"
                />
                <span
                  class="badge bg-success px-3 py-2 position-absolute top-0 start-0 m-3 menu-card-badge text-capitalize"
                  >${a.category}</span
                >
                <span
                  class="position-absolute top-0 end-0 m-3 badge bg-white text-success py-2 px-3 menu-card-price"
                  >$${a.price.toFixed(2)}</span
                >
              </div>
              <!-- Bottom -->
              <div class="card-body">
                <h5 class="card-title fw-bold text-success mb-2">
                  ${a.title}
                </h5>
              </div>
            </div>
          </a>`
    )
    .join("");
};

